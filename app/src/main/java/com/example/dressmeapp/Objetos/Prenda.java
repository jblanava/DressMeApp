package com.example.dressmeapp.Objetos;

public class Prenda
{
    public int id;
    public String nombre;
    public String color;
    public int tipo;
    public int talla;

    public Prenda(int i, String n, String c, int ti, int ta)
    {
        this.id = i;
        this.nombre = n;
        this.color = c;
        this.tipo = ti;
        this.talla = ta;
    }
}
