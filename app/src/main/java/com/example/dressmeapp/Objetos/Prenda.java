package com.example.dressmeapp.Objetos;

public class Prenda
{
    public Prenda(int i, String n, String c, int ti, int ta)
    {
        this.id = id;
        this.nombre = n;
        this.color = c;
        this.tipo = ti;
        this.talla = ta;
    }

    public String nombre;
    public String color;
    public int tipo;
    public int talla;

    public int id;
}
