package com.example.dressmeapp;

public class Prenda
{
    public Prenda(String n, String c, String ti, String ta)
    {
        this.nombre = n;
        this.color = c;
        this.tipo = ti;
        this.talla = ta;
    }

    public String nombre;
    public String color;
    public String tipo;
    public String talla;
}
